n = raw_input("Please enter a number to help me create a pattern:\n> ")
l = list(range(int(n)))
l[0] = " "
for each in l:
	if each != " ":
		l[l.index(each)] = "*"

for each in range(int(n)):
	print("".join(l))
	l.insert(l.index(" ")+1, l.pop(l.index(" ")))